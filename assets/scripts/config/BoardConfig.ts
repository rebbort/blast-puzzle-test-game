// Настройки игрового поля

export type TileColor = string; // тип цвета клетки, строка чтобы не привязываться к конкретной реализации

// Интерфейс конфигурации поля
export interface BoardConfig {
  cols: number; // ширина поля в колонках
  rows: number; // высота поля в строках
  tileSize: number; // размер одного спрайта в пикселях
  colors: TileColor[]; // список допустимых цветов тайлов
  superThreshold: number; // размер группы, после которого создаётся супер-тайл
  rngSeed?: string; // необязательно: фиксирует генератор для детерминизма
}

// Конфигурация по умолчанию
export const DefaultBoard: BoardConfig = {
  cols: 8, // восемь колонок удобно помещаются на экране
  rows: 10, // десять строк дают комфортную высоту уровня
  tileSize: 96, // 96 px соответствует размерам подготовленных спрайтов
  colors: ["red", "blue", "green", "yellow", "purple"], // базовые пять цветов обеспечивают разнообразие
  superThreshold: 5, // группа из пяти клеток создаёт супер-тайл
};
